CREATE DATABASE COCHES;

USE COCHES;

CREATE DOMAIN D_POTENCIA NUMERIC(4,0) CHECK (VALUE > 0);

CREATE TABLE COCHES (
	ID SERIAL PRIMARY KEY,
    MARCA VARCHAR(100),
    MODELO VARCHAR(100),
   	PESO NUMERIC(5),
    POTENCIA D_POTENCIA
);

CREATE TYPE T_RUEDA AS (RUEDA NUMERIC(3), LLANTA NUMERIC(2));

ALTER TABLE COCHES ADD RUEDA T_RUEDA;

